# Usamos el metodo .split() para guardar varios valores en diferentes variables 
txt1, txt2, txt3 = input("Introduce 3 nombres (separados por espacios) ").split()

# Sacamos los valores por pantalla para mostrar el resultado
print("El primer nombre es "+txt1)
print("El segundo nombre es "+txt2)
print("El tercer nombre es "+txt3)


